package com.eshret.talker.core

// Этот файл описывает метаданные одной сессии журнала eshret_talker.
// Сессия — это весь набор логов за один запуск приложения; здесь лежит то, что нужно
// для списка сессий: когда началась, когда была последняя активность и сколько записей.

data class EshretTalkerSession(
    // Это идентификатор сессии в виде времени старта (например, "2026-06-24_14-30-05-123").
    val id: String,
    // Это время старта сессии в миллисекундах.
    val startedAtMillis: Long,
    // Это время последней записи в сессии — по нему UI показывает «сколько назад закончена».
    val lastActivityAtMillis: Long,
    // Это число записей, сохранённых в сессии.
    val entryCount: Int,
)
